        ///
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e">Gesture event arguments.</param>
        private void OnGestureRecognized(object sender, GestureEventArgs e)
        {
            switch (e.GestureName)
            {
                case "Menu":
                    break;
                case "WaveRight":
                    break;
                case "WaveLeft":
                    break;
                case "JoinedHands":
                    //Gesture = "Joined Hands";
                    break;
                case "SwipeLeft":
                   // Gesture = "Swipe Left";
                    break;
                case "SwipeRight":
                    //Gesture = "Swipe Right";
                    break;
                case "SwipeUp":
                   // Gesture = "Swipe Up";
                    break;
                case "SwipeDown":
                    //Gesture = "Swipe Down";
                    break;
                case "ZoomIn":
                    //Gesture = "Zoom In";
                    break;
                case "ZoomOut":
                    //Gesture = "Zoom Out";
                    break;
                case "Surrender":
                    //Gesture = "Surrender";
                    break;

                default:
                    break;
            }

            //_clearTimer.Start();
        }
    }
}
